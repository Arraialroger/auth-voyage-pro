-- 1. TIPOS PERSONALIZADOS (ENUMS)
CREATE TYPE public.appointment_status_enum AS ENUM ('Scheduled', 'Confirmed', 'Completed', 'Cancelled', 'No-Show', 'Pending Confirmation', 'Patient Arrived');
CREATE TYPE public.certificate_type_enum AS ENUM ('attendance', 'medical_leave', 'fitness');
CREATE TYPE public.expense_category_enum AS ENUM ('rent', 'utilities', 'supplies', 'equipment', 'salary', 'marketing', 'maintenance', 'other');
CREATE TYPE public.expense_status_enum AS ENUM ('pending', 'paid');
CREATE TYPE public.goal_status_enum AS ENUM ('active', 'completed', 'cancelled');
CREATE TYPE public.installment_plan_status_enum AS ENUM ('active', 'completed', 'cancelled');
CREATE TYPE public.installment_status_enum AS ENUM ('pending', 'paid', 'overdue', 'cancelled');
CREATE TYPE public.payment_method_enum AS ENUM ('cash', 'credit_card', 'debit_card', 'pix', 'bank_transfer', 'insurance', 'boleto');
CREATE TYPE public.payment_status_enum AS ENUM ('pending', 'completed', 'cancelled', 'refunded', 'partial');
CREATE TYPE public.prescription_type_enum AS ENUM ('simple', 'controlled', 'special');
CREATE TYPE public.specialization_enum AS ENUM ('Cirurgião-Dentista', 'Ortodontista');
CREATE TYPE public.tooth_face_enum AS ENUM ('oclusal', 'mesial', 'distal', 'vestibular', 'lingual', 'incisal');
CREATE TYPE public.tooth_status_enum AS ENUM ('higido', 'cariado', 'obturado', 'extraido', 'tratamento_canal', 'coroa', 'implante', 'ausente', 'fratura');
CREATE TYPE public.transaction_type_enum AS ENUM ('payment', 'refund', 'discount');
CREATE TYPE public.treatment_plan_item_status_enum AS ENUM ('pending', 'in_progress', 'completed', 'cancelled', 'awaiting_payment');
CREATE TYPE public.treatment_plan_status_enum AS ENUM ('awaiting_payment', 'approved', 'in_progress', 'completed', 'cancelled');

-- 2. CRIAÇÃO DAS TABELAS BÁSICAS E DE NEGÓCIO (EM ESTADO FINAL DE PRODUÇÃO)

CREATE TABLE public.patients (
    id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
    full_name text NOT NULL,
    contact_phone text NOT NULL,
    cpf text,
    birth_date date,
    medical_history_notes text,
    created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE public.professionals (
    id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
    full_name text NOT NULL,
    specialization public.specialization_enum NOT NULL,
    user_id uuid,
    contact_phone text,
    clinic_name text,
    clinic_cnpj text,
    clinic_phone text,
    clinic_address text,
    professional_registry text,
    registry_uf text,
    created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE public.staff_profiles (
    id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
    role text NOT NULL,
    user_id uuid
);

CREATE TABLE public.treatments (
    id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
    treatment_name text NOT NULL,
    default_duration_minutes integer NOT NULL DEFAULT 30,
    cost numeric,
    description text
);

CREATE TABLE public.appointments (
    id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
    patient_id uuid REFERENCES public.patients(id),
    professional_id uuid REFERENCES public.professionals(id),
    treatment_id uuid REFERENCES public.treatments(id),
    treatment_plan_item_id uuid, -- Relacionamento circular, fkey adicionada ao final
    appointment_start_time timestamp with time zone NOT NULL,
    appointment_end_time timestamp with time zone NOT NULL,
    status public.appointment_status_enum NOT NULL DEFAULT 'Scheduled',
    notes text,
    is_squeeze_in boolean DEFAULT false,
    confirmation_sent_at timestamp with time zone,
    last_reminder_sent_at timestamp with time zone,
    created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE public.waiting_list (
    id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
    patient_id uuid REFERENCES public.patients(id) NOT NULL,
    professional_id uuid REFERENCES public.professionals(id) NOT NULL,
    treatment_id uuid REFERENCES public.treatments(id),
    notes text,
    created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE public.communication_logs (
    id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
    patient_id uuid REFERENCES public.patients(id),
    appointment_id uuid REFERENCES public.appointments(id),
    direction text NOT NULL,
    communication_content text NOT NULL,
    created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE public.professional_schedules (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    professional_id uuid REFERENCES public.professionals(id),
    day_of_week integer NOT NULL,
    start_time text NOT NULL,
    end_time text NOT NULL
);

CREATE TABLE public.patient_documents (
    id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
    patient_id uuid REFERENCES public.patients(id) NOT NULL,
    file_name text NOT NULL,
    file_path text NOT NULL,
    file_size integer,
    mime_type text,
    uploaded_by uuid,
    created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE public.expenses (
    id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
    description text NOT NULL,
    amount numeric NOT NULL,
    due_date date NOT NULL,
    payment_date date,
    category public.expense_category_enum NOT NULL,
    status public.expense_status_enum NOT NULL DEFAULT 'pending',
    notes text,
    created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE public.treatment_plans (
    id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
    patient_id uuid REFERENCES public.patients(id) NOT NULL,
    professional_id uuid REFERENCES public.professionals(id) NOT NULL,
    title text,
    status public.treatment_plan_status_enum NOT NULL DEFAULT 'awaiting_payment',
    total_cost numeric,
    notes text,
    created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL,
    updated_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE public.treatment_plan_items (
    id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
    treatment_plan_id uuid REFERENCES public.treatment_plans(id) NOT NULL,
    procedure_description text NOT NULL,
    tooth_number integer,
    estimated_cost numeric,
    status public.treatment_plan_item_status_enum NOT NULL DEFAULT 'pending',
    priority integer,
    notes text,
    treatment_id uuid REFERENCES public.treatments(id),
    appointment_id uuid REFERENCES public.appointments(id),
    scheduled_date timestamp with time zone,
    completed_at timestamp with time zone,
    created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

