CREATE TABLE public.odontogram_records (
    id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
    patient_id uuid REFERENCES public.patients(id) NOT NULL,
    tooth_number integer NOT NULL,
    status public.tooth_status_enum NOT NULL DEFAULT 'higido',
    notes text,
    last_updated_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL,
    last_updated_by uuid
);

CREATE TABLE public.tooth_procedures (
    id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
    patient_id uuid REFERENCES public.patients(id) NOT NULL,
    professional_id uuid REFERENCES public.professionals(id),
    tooth_number integer NOT NULL,
    faces public.tooth_face_enum[],
    procedure_type text NOT NULL,
    procedure_date date NOT NULL DEFAULT CURRENT_DATE,
    status_before public.tooth_status_enum,
    status_after public.tooth_status_enum NOT NULL,
    material_used text,
    notes text,
    status text,
    created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE public.payments (
    id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
    patient_id uuid REFERENCES public.patients(id) NOT NULL,
    treatment_plan_id uuid REFERENCES public.treatment_plans(id),
    subtotal numeric NOT NULL,
    discount_type text,
    discount_value numeric,
    discount_amount numeric NOT NULL DEFAULT 0,
    total_amount numeric NOT NULL,
    total_installments integer NOT NULL DEFAULT 1,
    installment_number integer NOT NULL DEFAULT 1,
    payment_date timestamp with time zone NOT NULL DEFAULT timezone('utc'::text, now()),
    registered_by uuid NOT NULL,
    notes text,
    created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE public.payment_entries (
    id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
    payment_id uuid REFERENCES public.payments(id) NOT NULL,
    payment_method public.payment_method_enum NOT NULL,
    amount numeric NOT NULL,
    created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE public.payment_splits (
    id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
    payment_id uuid REFERENCES public.payments(id) NOT NULL,
    professional_id uuid REFERENCES public.professionals(id) NOT NULL,
    amount numeric NOT NULL,
    percentage numeric NOT NULL,
    status text NOT NULL DEFAULT 'pending',
    payment_date timestamp with time zone,
    created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE TABLE public.expense_installments (
    id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
    expense_id uuid REFERENCES public.expenses(id) NOT NULL,
    installment_number integer NOT NULL,
    amount numeric NOT NULL,
    due_date date NOT NULL,
    status public.expense_status_enum NOT NULL DEFAULT 'pending',
